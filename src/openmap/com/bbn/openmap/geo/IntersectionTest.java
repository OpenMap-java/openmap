    public double[] swap(double[] input) {
        double[] poly = new double[_Polygon.length];
        for (int i = 0; i < _Polygon.length - 1; i += 2) {
            poly[i + 1] = _Polygon[i];
            poly[i] = _Polygon[i + 1];
        }
        return poly;
    }
    
    // @Test
    public void testIsSelfIntersectingPoly() {
        double[] poly = swap(_Polygon);
        
        //assertTrue(Intersection.isSelfIntersectingPoly(toFloatArray(_BadPolygon)));
        assertFalse(Intersection.isSelfIntersectingPoly(poly));
    }

